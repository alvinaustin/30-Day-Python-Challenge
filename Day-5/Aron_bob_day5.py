# This will import math module
import math


sqrt=math.sqrt(90)
print("Square Root of '90'=",sqrt)

power=math.pow(5,8)
print("Power of 5 raised to 8=",power)

floor=math.floor(4.8)
print("Largest integer less than or equal to 4.8=",floor)

ceil=math.ceil(4.8)
print("Largest integer greater than or equal to 4.8=",ceil)

fact=math.factorial(8)
print("The factorial of 8=",fact)


#constants in math module
print("The Value of pi=",math.pi)
